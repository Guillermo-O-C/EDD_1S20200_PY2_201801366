    void ListCategories(){    
            list.clear();
            NODO_AVL x =  usaclibrary.USACLibrary.PublicLibrary.getRoot();
            Traverse(x);
            jList1.setModel(list);          
    }
    void Traverse(NODO_AVL x){
        if(x!=null){
            if(x.getLeft()!=null){
                Traverse(x.getLeft());
            }
            this.list.addElement(x.getValue());
            if(x.getRight()!=null){
                Traverse(x.getRight());
            }
        }
    }
