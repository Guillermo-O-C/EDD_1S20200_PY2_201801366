    public void AddItems(NODO_AVL x){
        if(x==null){            
            return;
        }
        if(x.getLeft()!=null){
            AddItems(x.getLeft());
        }
        jComboBox1.addItem(new ComboItem(x.getValue(), x.getValue()));
        if(x.getRight()!=null){
            AddItems(x.getRight());
        }
    }
    
