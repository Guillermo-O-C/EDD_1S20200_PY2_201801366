    public void AddItems(NODO_AVL x){
        if(x==null){            
            return;
        }
        jComboBox1.addItem(new ComboItem(x.getValue(), x.getValue()));
        if(x.getLeft()!=null){
            AddItems(x.getLeft());
        }
        if(x.getRight()!=null){
            AddItems(x.getRight());
        }
    }
    
