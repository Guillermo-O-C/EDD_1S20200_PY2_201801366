    void PrintTree(NODO_B x){
        for(int i =0;i<5;i++){
                if(x.getShelf()[i]!=null){
                    list.addElement(x.getShelf()[i].getTitulo());
                }
            }
        for(int i =0;i<5;i++){
            if(x.getBranches()[i]!=null){
                PrintTree(x.getBranches()[i]);
            }            
        }
    }
